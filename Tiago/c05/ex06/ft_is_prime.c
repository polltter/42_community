int	ft_is_prime(int nb)
{
	int	i;

	i = nb -1;
	if (nb <= 1)
		return (0);
	else if (nb == 2)
		return (1);
	while (i > 1)
	{
		if (nb % i == 0)
			return (0);
		i--;
	}
	return (1);
}
